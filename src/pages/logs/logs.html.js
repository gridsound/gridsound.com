"use strict";

GSUsetTemplate( "gscoLogs", () =>
	$.$div( { class: "page", id: "logsPage" },
		$.$div( { id: "logsPage_title" }, "Logs 🤫" ),
		$.$div( { id: "logsPage_users5min" },
			$.$span( null, "users ping (5min): " ),
		),
		$.$div( { id: "logsPage_users24h" },
			$.$span( null, "users ping (24h): " ),
		),
		$.$div( { id: "logsPage_logs" } ),
	),
);

GSUsetTemplate( "gscoLogsLog", p =>
	$.$div( { class: "logsPage-log" },
		$.$div( { class: "logsPage-log-body" },
			$.$link( { href: `#/u/${ p.username }` },
				$.$bold( null, p.username ),
				// $.$span( null, ` (${ p.firstname },${ p.lastname })` ),
			),
		),
		$.$div( { class: "logsPage-log-action", "data-action": p.action },
			$.$icon( { icon: p.icon } ),
			$.$span( null, p.action ),
		),
		$.$div( { class: "logsPage-log-target" },
			p.cmpid && $.$link( { href: `#/cmp/${ p.cmpid }` },
				$.$icon( { icon: "music" } ),
				$.$bold( null, p.cmpname ),
			),
			p.utarget_id && $.$link( { href: `#/u/${ p.utarget_username }` },
				$.$icon( { icon: "musician" } ),
				$.$bold( null, p.utarget_username ),
			),
			p.action.startsWith( "SEARCH-" ) && $.$elem( "i", null, `"${ p.idtarget }"` ),
			p.action === "RESEND-EMAIL" && $.$span( null, p.idtarget ),
		),
		$.$span( { class: "logsPage-log-when" }, p.time ),
	),
);
